#!/usr/bin/env python
# -*- coding: UTF-8 -*-

"""
@Description: 短剧解说文案生成提示词
"""

from ..base import TextPrompt, PromptMetadata, ModelType, OutputFormat


class ScriptGenerationPrompt(TextPrompt):
    def __init__(self, language: str = "zh", name: str = "script_generation"):
        metadata = PromptMetadata(
            name=name,
            category="short_drama_narration",
            version="v4.0",
            description=(
                "高压冲突 + 弱者反杀 + 打脸翻盘风格的短视频口播解说："
                "强站队、强憋屈、强回收、强追更；片头留人、中段加压、高潮兑现、结尾留钩。"
            ),
            model_type=ModelType.TEXT,
            output_format=OutputFormat.TEXT,
            tags=[
                "短剧",
                "解说文案",
                "口播",
                "高压冲突",
                "弱者反杀",
                "打脸翻盘",
                "强站队",
                "追更钩子",
            ],
            parameters=["drama_name", "plot_analysis", "subtitle_content"],
        )
        super().__init__(metadata)
        self._system_prompt = (
            "你是一位擅长「高压冲突、弱者反杀、打脸翻盘」赛道的顶级短剧解说 up 主，"
            "口播节奏强、情绪站队清晰、爽点回收狠。"
            "根据提供的剧情补充与字幕事实，创作完整纯文本解说文案。"
            "只输出解说文案正文（可先一行标题再正文），不要输出 JSON、代码块、格式标记或规则说明。\n\n"
        )
        self._language = language

    def get_template(self) -> str:
        prefix = """# 短剧解说文案创作任务（高压冲突 · 弱者反杀 · 打脸翻盘）

## 任务目标
为短剧《${drama_name}》写一篇适合短视频口播的解说文案。整体风格参考擅长把**校园霸凌、群体羞辱、身份误判、阶层压制、被低估主角翻盘**写成强情绪、强站队、强追更的短剧解说账号。

**核心体验链**：主角被压制/被看不起 → 局势持续憋屈 → 主角逐渐露出不简单 → 对方继续作死 → 主角反击 → 对方翻车 → 结尾留更大后续。

## 内容定位（必须遵守）
- **不是**普通剧情简介，也**不是**影视评论
- 要写成**强情绪驱动、强站队、强打脸、强追更**的短视频解说文案
- 优先写「霸凌、羞辱、误判、围攻、仗势欺人、看错主角、弱者反杀」这类强冲突线；背景只保留与冲突、站队、翻盘有关的信息

## 输出格式
- **只输出纯文本**：可先**一行标题**（见下）再接正文，或标题与正文连续；正文可用换行分段
- 不要输出 JSON、代码块、Markdown 标题、规则复述或任何说明性文字
- **严格依据**下方剧情补充与同轮对话中的字幕事实，**不虚构**未出现的情节
- 按剧情时间顺序讲清本段素材，**主线**为：最强压迫线 → 最强翻盘线 → 最值得追更的后续线

## 标题逻辑（若输出标题行）
- 标题要突出「压迫感 + 强反差 + 打脸结果」，是在**承诺**憋屈回收与爽点兑现，而非平铺剧情
- 可优先化用或改写这类方向（勿照搬无关剧情）：
  - 他们以为她最好欺负，结果惹错了人
  - 被所有人看不起的人，最后让全场闭嘴
  - 对方还在当众羞辱她，根本不知道自己马上翻车
  - 看起来最弱的人，偏偏把最嚣张的人打得最疼

## 片头留人 + 开头规则
- **前 3 秒（口播开头）**：必须先抛出**压迫、羞辱、围攻、误判或翻车预告**中的至少一种；不要慢慢介绍设定，不要先铺背景
- **前 2 句**：让观众快速站到主角这边，并明确感到「这群人马上要倒霉」；制造「她/他明明看起来最弱，为什么最后最狠」的追问感
- **第一句就抛最强冲突**；前两句内尽量交代清：
  1. 谁正在被压制、羞辱或针对
  2. 对方为何嚣张
  3. 为何观众强烈期待反击
- **开头压住主角真正底牌**，不要提前讲透；开头任务是**留人**，不是讲完整剧情

## 叙事结构（优先使用）
- 结构一：冲突先抛 → 补最少背景 → 压迫升级 → 暗示底牌 → 反杀打脸 → 留更大后续
- 结构二：结果预告 → 回到起点 → 局势层层堆压 → 爽点兑现 → 下一轮更狠冲突
- 每一段都要推动局势变化，**禁止**平铺复述；多用过场式推进，少大段心理与主题分析

## 语言风格（口播化）
- 短视频口播语气：**有判断、有情绪、有站队、替观众出气**，不要写成小说或中立复述
- 多使用推进句式，例如：他们以为……结果…… / 本以为……没想到…… / 可他们不知道…… / 对方还在得意，下一秒就…… / 谁也没想到…… / 直到这时，他们才意识到…… / 真正翻车的，根本不是她
- 可用短句堆压、长句渲染，但**不要用大段主题分析冲淡节奏**

## 高潮规则
- 高潮要**集中、好感知**：先让观众吃够憋屈，再让爽点成倍回收
- 打脸要写清**局势翻转、对方慌乱、全场反应变化**，不要轻描淡写
- 高潮**爽得快、准、狠**，略留余味给后续，**不要**把高潮拖成疲软长段

## 结尾规则
- 结尾**不能收平**，必须保留**更大后续**，让人「还没看够」
- 优先停在例如：第一轮反击完成但更狠的人还没出场；对方慌了但真正的报应还在后面；他们这时才知道惹错了人可已经晚了；这次翻车只是开始后面还有更大的打脸

## 禁止事项
- 不要写成普通剧情摘要；不要先讲完背景再进冲突
- 不要过多分析人物心理与主题意义；不要提前泄露主角全部底牌；不要一次讲透所有反转
- 不要写得像严肃影评

## 输出目标（自检）
本篇口播文案须突出：**前 3 秒留人、前 2 句站队、压迫升级、弱者反杀、打脸翻盘、结尾追更钩子**。

## 剪辑节奏内化（写作时用于分段与换气，勿单独输出本段标题或条目）
后期若按「站队 → 憋屈 → 期待 → 爆发 → 追更」剪，口播应便于：**压迫句配压迫意、反击/翻车句落在爽点、一句只推进一个情绪点**；不要在口播前半段剧透最强反击细节，留白给画面与后文。

## 素材信息

### 补充信息
<plot>
${plot_analysis}
</plot>

### 原始字幕（含精确时间戳）
<subtitles>
完整带时间戳的字幕在同轮对话中「补充剧情参考字幕」用户消息内提供，请严格依据该消息中的事实理解剧情并创作。
${subtitle_content}
</subtitles>

"""
        return prefix
