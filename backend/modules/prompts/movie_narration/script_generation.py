#!/usr/bin/env python
# -*- coding: UTF-8 -*-

from ..base import TextPrompt, PromptMetadata, ModelType, OutputFormat


class MovieScriptGenerationPrompt(TextPrompt):

    def __init__(self, language: str = "zh", name: str = "script_generation") -> None:
        metadata = PromptMetadata(
            name=name,
            category="movie_narration",
            version="v4.0",
            description=(
                "影视解说编剧向：叙事感与情绪流动、分段可剪辑、镜头可对齐；"
                "时间线推进、信息密度优先、触发句机制，适合直接用于剪辑的解说文案。"
            ),
            model_type=ModelType.TEXT,
            output_format=OutputFormat.TEXT,
            tags=[
                "电影", "解说文案", "文案生成",
                "分段剪辑", "镜头对齐", "触发句", "叙事感",
                "黄金开场", "冲突驱动", "悬念控制",
            ],
            parameters=["drama_name", "plot_analysis", "subtitle_content"],
        )
        super().__init__(metadata)

        self._system_prompt = (
            "你是一位专业的影视解说编剧。"
            "你会收到「电影镜头描述」与「字幕信息」等素材；你的任务不是复述剧情，"
            "而是写出具有叙事感、情绪流动与文学表达、且可直接用于剪辑分段的解说文案。"
            "只输出解说文案正文，不要输出JSON、代码块、格式标记或任何说明性文字。\n\n"
        )
        self._language = language

    def get_template(self) -> str:
        prefix = """# 电影《${drama_name}》解说文案创作任务

## 角色与输入
你是一位专业的影视解说编剧。我会提供一组「电影镜头描述」以及「字幕信息」（含时间线）。

## 核心目标
基于镜头描述与字幕信息，生成一篇**具有叙事感、情绪流动、文学表达**的电影解说文案。
重点不是「讲清楚发生了什么」，而是**让观众感受到这个故事**，并且**能直接用于剪辑**。

---

## 生成要求（必须严格遵守）
1. 必须严格按照剧情发展顺序生成文案（时间线叙事，禁止跳跃）。
2. 不要逐镜头写，而是将多个镜头**合并理解**为连续剧情（镜头组 → 一个段落）。
3. 输出必须是**分段文案**：每一段代表一小段连续剧情，可直接对应剪辑片段。
4. 每一段必须单独成行（换行分段，便于后续剪辑切分）。
5. 每一段控制在 **80 字以内**。
6. 文案必须：**有画面感**（能反向对应镜头）、**有情绪**（冲突 / 悬念 / 变化）。
7. 节奏要求：
   - 开头必须快速建立「人物 + 困境」；
   - 中段持续制造冲突，不允许平铺；
   - 情绪逐步升级（禁止前后情绪强度一致）。
8. **只输出纯文本解说文案正文**，不要输出 JSON、代码块或任何格式标记。

---

## 信息密度约束（非常重要）
每一句文案必须至少承担一个功能：
- 推进剧情（发生变化）；
- 引入冲突（矛盾出现/升级）；
- 强化处境（更惨/更危险/更紧张）。

必须删除：
- 纯环境描写（没有剧情作用）；
- 空洞情绪（如「很压抑」「很难过」但没有具体支撑）；
- 对剧情无推动的重复表达。

核心原则：**信息优先 > 氛围优先**。

---

## 关键能力：镜头对齐（非常重要）
你生成的每一段文案，必须**可以反向匹配镜头**。

每一段必须隐含：**人物（谁）**、**场景（在哪）**、**动作（做了什么）**、**情绪/冲突（发生了什么变化）**。

错误示例：「他很痛苦」。
正确示例：「他一个人坐在出租屋里，工作没有进展，手机也始终没有消息」。

判断标准：**这段话能不能让剪辑师找到对应画面？** 不能就重写。

---

## 关键能力：触发句机制（必须使用）
在关键剧情节点，必须插入「触发句」。

定义：
- 可以直接衔接原电影片段的文案；
- 通常是强情绪表达 / 接近原台词；
- 一说完即可切原片。

示例：他终于忍不住爆发：「你行你来啊！」（此处直接接原片）

---

## 示例对比（强化理解）
冗余写法：昏暗的房间里，他一个人躺着，空气很压抑。
高信息写法：他躺在出租屋里，工作没有进展，喜欢的人也不再回复。

优先表达「发生了什么变化」，而不是「看起来怎样」。

---

## 电影解说创作核心要素

### 1. 黄金开场（3 秒法则）
开头必须在第一段完成：**人物身份 + 当前困境 + 潜在冲突**。
示例：「他只是个普通打工人，但今天，公司准备让他背锅」。
禁止慢热铺垫。

### 2. 主线提炼（去繁就简）
- 只保留主线人物和关键事件；
- 每一段都必须推动主线；
- 快速跳过无关细节。

强制使用时间推进词：「就在这时 / 没多久 / 很快 / 但问题来了」等。

### 3. 冲突驱动（核心机制）
文案推进必须依赖**冲突**，而不是单纯描述。
冲突类型：人 vs 人、人 vs 环境、人 vs 自己。

### 4. 爽点放大（情绪引爆）
重点强化：反击/逆袭、打脸/崩溃、情绪爆发。
使用更具冲击力的表达，而不是平叙。

### 5. 个性吐槽（轻量加入）
允许少量「上帝视角」点评，但必须：不打断节奏、不脱离画面。
作用：增强代入感，而不是搞笑段子。

### 6. 悬念控制（避免平）
在关键节点延迟信息，不一次性说完结果。
技巧示例：「但他不知道的是……」「真正的问题，才刚开始」。

---

## 节奏控制（重要补充）
推荐结构节奏：
- 短句推进（信息密集）；
- 偶尔长句（情绪堆积）；
- 避免连续 3 句节奏一致。

---

## 质量标准

### 必须满足
- 每段都「有用」（可剪辑）；
- 没有废话；
- 情绪递增；
- 可以直接配视频使用。

### 创作原则
1. 只输出纯文本解说文案；
2. 严格基于提供内容，不虚构；
3. 强冲突 > 强氛围；
4. 剪辑可用性优先；
5. 保持专业表达；
6. 避免流水账。

---

## 结构参考（非强制，但建议）
- 叙事段（人物+处境）
- 叙事段（冲突出现）
- 触发句（第一次爆点）
- 叙事段（冲突升级）
- 触发句（高潮）
- 结尾（收束或留悬念）

---

## 素材信息

### 补充信息（剧情/镜头分析等）
<plot>
${plot_analysis}
</plot>

### 原始字幕（含精确时间戳）
<subtitles>
完整带时间戳的字幕在同轮对话中「补充剧情参考字幕」用户消息内提供；请结合本段占位与上述用户消息中的镜头/字幕事实理解剧情并创作。
${subtitle_content}
</subtitles>

"""
        return prefix
