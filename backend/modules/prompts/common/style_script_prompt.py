class CommonStyleScriptPrompt(TextPrompt):
    """官方风格模板：从 common 子目录加载 .md，短剧类目下用语适配。"""

    def __init__(
        self,
        *,
        category: str,
        label: str,
        display_name: str,
        relative_md_path: str,
        description: str,
        tags: List[str],
    ) -> None:
        metadata = PromptMetadata(
            name=display_name,
            category=category,
            version="v1.0",
            description=description,
            model_type=ModelType.TEXT,
            output_format=OutputFormat.TEXT,
            tags=tags,
            parameters=["drama_name", "plot_analysis", "subtitle_content"],
        )
        super().__init__(metadata)
        self._label = label
        self._relative_md_path = relative_md_path

    def get_template(self) -> str:
        base = Path(__file__).resolve().parent
        p = base / self._relative_md_path
        body = p.read_text(encoding="utf-8")
        if self._label == "短剧":
            body = _adapt_markdown_for_short_drama(body)
        return body.rstrip() + "\n" + _MATERIAL_SUFFIX


def register_common_style_prompts_for_category(category: str, label: str) -> None:
    """为电影或短剧类目注册上述全部风格模板。"""
    from ..prompt_manager import PromptManager

    for entry in _STYLE_ENTRIES:
        desc = str(entry["description"])
        tags = list(entry["tags"]) + [label, "官方模板"]
        p = CommonStyleScriptPrompt(
            category=category,
            label=label,
            display_name=entry["name"],
            relative_md_path=entry["md"],
            description=desc,
            tags=tags,
        )
        PromptManager.register_prompt_cls(p, is_default=False)


